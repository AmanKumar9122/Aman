public class q3 {

        public static void main(String[] args) {
            int a = 10;
            int b = 5;
    
            // Addition
            int sum = a + b;
            System.out.println("Sum: " + sum);
    
            // Subtraction
            int difference = a - b;
            System.out.println("Difference: " + difference);
    
            // Multiplication
            int product = a * b;
            System.out.println("Product: " + product);
    
            // Division
            double quotient = (double) a / b;
            System.out.println("Quotient: " + quotient);
    
            // Modulo
            int remainder = a % b;
            System.out.println("Remainder: " + remainder);
    
            // Bitwise AND
            int andOperation = a & b;
            System.out.println("Bitwise AND: " + andOperation);
    
            // Bitwise OR
            int orOperation = a | b;
            System.out.println("Bitwise OR: " + orOperation);
    
            // Bitwise XOR
            int xorOperation = a ^ b;
            System.out.println("Bitwise XOR: " + xorOperation);
    
            // Left shift
            int leftShift = a << 2;
            System.out.println("Left shift: " + leftShift);
    
            // Right shift
            int rightShift = a >> 2;
            System.out.println("Right shift: " + rightShift);
    
            // Bitwise NOT
            int notOperation = ~a;
            System.out.println("Bitwise NOT: " + notOperation);
        }
    }

