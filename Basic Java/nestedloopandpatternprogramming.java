//printing a square pattern using stars.
public class nestedloopandpatternprogramming {
    public static void main(String[] args) {
        int n=10;
        for(int j=0;j<n;j++){
            for(int i=0;i<n;i++){
                System.out.print(" * ");
            }
            System.out.println();
        }
    }
}
