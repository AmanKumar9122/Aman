public class arithmeticoperator {
    public static void main(String[] args) {
        int num1 = 30;
        int num2 = 10;
        System.out.println(num1+num2);//addition operator
        System.out.println(num1-num2);//subtraction operator
        System.out.println(num1*num2);//multiplication operator
        System.out.println(num1/num2);//division operator
        System.out.println(num1%num2);//modulo operator(it gives reminder)
        System.out.println(2%8);//if value of numerator is less then it gives numerator as result
    }    
}