public class relationaloperator {
    public static void main(String[] args){
        int a=10;
        int b=10;
        System.out.println(a<b);//f
        System.out.println(a>b);//f
        System.out.println(a<=b);//t
        System.out.println(a>=b);//t
        System.out.println(a<=b==a>=b);//t
    }
    
}
